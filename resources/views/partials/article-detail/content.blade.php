            <p class="mt-8 text-lg leading-[1.75] text-[var(--color-charcoal)]/80 lg:text-[18px]">
                Dawn breaks on Day 19. The Traverse — Pitch 32 — glows pale gold in the early
                light. I have rehearsed this sequence a thousand times in my head. The first
                undercling. The drop-knee. The dynamic move to the sloper. The mantel. The exit.
            </p>

            <p class="mt-6 text-lg leading-[1.75] text-[var(--color-charcoal)]/80 lg:text-[18px]">
                I tie in. Kevin feeds rope. The first hold is a razor-thin crimp barely wider than
                a credit card edge. I weight it. I move.
            </p>

            <p class="mt-6 text-lg leading-[1.75] text-[var(--color-charcoal)]/80 lg:text-[18px]">
                Seventy-five feet out. Halfway. My left foot smears against a polished slab. My
                right hand floats out toward the dyno. I don't think. I jump.
            </p>

            <p class="mt-6 text-lg leading-[1.75] text-[var(--color-charcoal)]/80 lg:text-[18px]">
                And then, somehow, impossibly, the finishing holds are in my fingers and I'm
                pulling onto the ledge and Kevin is screaming from forty feet below and the
                granite, after nineteen days, lets us go.
            </p>

            <h2 class="mt-16 font-display text-3xl font-bold leading-[1.15] tracking-[-0.025em] text-[var(--color-charcoal)] lg:text-[36px]">
                What the Wall Teaches
            </h2>

            <p class="mt-8 text-lg leading-[1.75] text-[var(--color-charcoal)]/80 lg:text-[18px]">
                The Dawn Wall taught me that limits are more flexible than they appear from the
                ground. That fear is information, not a verdict. That the difference between a
                climber and a person who has tried climbing is mostly how many times you have been
                willing to fall.
            </p>

            <p class="mt-6 text-lg leading-[1.75] text-[var(--color-charcoal)]/80 lg:text-[18px]">
                But mostly, it taught me that the summit isn't really the point. The point is the
                eighteen days that came before it. The point is the language of granite and rope
                and trust that you only learn by living inside it long enough for the world below
                to feel like a rumor.
            </p>

            <p class="mt-6 text-lg leading-[1.75] text-[var(--color-charcoal)]/80 lg:text-[18px]">
                That's what El Capitan does. It doesn't care about your résumé, your training
                cycle, or your sponsorship deal. It only cares whether you can return to the wall
                tomorrow with the same patience you brought today. And if you can, eventually,
                it lets you up.
            </p>
