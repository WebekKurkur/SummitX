                    “On the wall, time collapses. Days blur into a single long present tense. The
                    only thing that matters is the next move, and the next, and the next.”
                </p>
            </blockquote>

            <p class="mt-12 text-lg leading-[1.75] text-[var(--color-charcoal)]/80 lg:text-[18px]">
                The weather window was narrowing. January in Yosemite is a season of margins —
                clear cold mornings followed by storms that can pin you to the wall for days. We
                had maybe seventy-two hours before the next system arrived. Pitch 32 wasn't going
                to wait.
            </p>
