                The Ascent
            </h2>

            <p class="mt-8 text-lg leading-[1.75] text-[var(--color-charcoal)]/80 lg:text-[18px]">
                Each pitch is a puzzle written in stone. Pitch 14 — a sustained 5.14 traverse where
                the holds are so small that chalk barely sticks — took us three days to complete.
                Three days hanging in our portaledges, eating cold ramen and studying the face like
                cartographers mapping a country no one has ever named.
            </p>

            <p class="mt-6 text-lg leading-[1.75] text-[var(--color-charcoal)]/80 lg:text-[18px]">
                But Pitch 15 — my personal demon — was worse. A blank slab of granite where the
                only feature is a half-inch crystalline edge that has to be loaded with your full
                weight while you stand on a smear of nothing. I fell on it eleven times. On the
                twelfth, I didn't.
            </p>

            <blockquote class="mt-12 border-l-4 border-[var(--color-forest)] bg-white/60 px-8 py-7 lg:px-10 lg:py-8">
