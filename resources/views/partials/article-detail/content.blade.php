                The Preparation
            </h2>

            <p class="mt-8 text-lg leading-[1.75] text-[var(--color-charcoal)]/80 lg:text-[18px]">
                You don't wake up one morning and decide to climb the Dawn Wall. The route exists
                in a language only a handful of people on earth speak fluently — pitches measured
                in microscopic crystals, holds smaller than a pencil eraser, sequences that demand
                muscle memory built over months of failed attempts.
            </p>

            <p class="mt-6 text-lg leading-[1.75] text-[var(--color-charcoal)]/80 lg:text-[18px]">
                The training regimen was obsessive. Hangboard sessions every other day for two
                years. Weighted pull-ups in our kitchen at midnight. Endless laps on the Cookie
                Cliff and Pat and Jack practicing fall after fall after fall — because the Dawn
                Wall doesn't reward people who fear falling. It rewards people who have made peace
                with it.
            </p>
        </div>
    </div>

